package com.Switchcase;

public class Switch {
	
	
	public static void main(String[] args) {
		
		int a = 3;
		
				
				switch (a) {
			
				case 1:
					
					System.out.println("One");
					
					break;
					
				case 2:
					
					System.out.println("TWO");
					
					break;
					
				case 3:
					
					System.out.println("THREE");
					
					break;
					
				default:
					
					System.out.println("WELCOME");
					break;
				}
		
	}

}
